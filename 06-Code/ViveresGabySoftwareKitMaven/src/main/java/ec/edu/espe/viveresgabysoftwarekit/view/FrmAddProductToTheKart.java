    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        MarketHandler marketHandler = new MarketHandler();
        posibleProducts = marketHandler.searchProduct(txtFindProduct.getText());
        DefaultListModel<String> listModel = new DefaultListModel<>();
        if (posibleProducts.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se han encontrado productos con ese nombre");
        } else {
            for (Product posibleProduct : posibleProducts) {
                listModel.addElement(posibleProduct.getName() + posibleProduct.getDescription());
            }
        }

        lstPosibleproduct.setModel(listModel);


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        posibleProducts.get(lstPosibleproduct.getSelectedIndex());
        System.out.println(spnQuantity.getValue());
        MarketHandler marketHandler = new MarketHandler();
        marketHandler.addToTheKart(new ProductItem(posibleProducts.get(lstPosibleproduct.getSelectedIndex()), (int) spnQuantity.getValue()));
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

