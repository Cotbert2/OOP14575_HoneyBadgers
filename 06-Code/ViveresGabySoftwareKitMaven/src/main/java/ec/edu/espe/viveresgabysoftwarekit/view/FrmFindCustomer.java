    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        MarketHandler marketHandler = new MarketHandler();
        marketHandler.getCustomerById(txtId.getText());
        customerData = marketHandler.getCustomerById(txtId.getText());
        if (customerData == null) {
            if (JOptionPane.showConfirmDialog(null, "No se ha encontrado ning√∫n cliente con esa c√©dula, Desea crearlo?") == JOptionPane.YES_OPTION) {
                this.dispose();
                this.setVisible(false);
                new FrmNewCustomer().setVisible(true);
            }
        } else {
            txtEmail.setText(customerData.getEmail());
            txtName.setText(customerData.getFullname());
            txtAddress.setText(customerData.getAddress());
            txtPhone.setText(customerData.getPhone());
            btnSaveCustomer.setEnabled(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

