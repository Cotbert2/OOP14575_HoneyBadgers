    public boolean canSend() {
        boolean status = false;
        if (cmbxCustomerType.getSelectedItem().toString().equals("Datos")) {
            btnEndSell.setEnabled(false);
        } else {
            btnEndSell.setEnabled(true);
        }
        return status;
    }

