    private void btnEndSellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEndSellActionPerformed
        // TODO add your handling code here:
        UIManager.put("OptionPane.yesButtonText", "Sí");
        UIManager.put("OptionPane.noButtonText", "No");
        JOptionPane.showConfirmDialog(null, "Deseas enviar la factura al correo del cliente?");
        
        MarketHandler marketHandler = new MarketHandler();
        Bill bill = new Bill(marketHandler.getCustomer(), marketHandler.getKart(), null);
        bill.saveCurrentBill();
        Validations validations = null;
        try {
            bill.doBill(new Transaction(true, 100, marketHandler.getCustomer()), marketHandler.getKart(), null, marketHandler.getCustomer(), validations);
        } catch (MessagingException ex) {
            Logger.getLogger(FrmNewSell.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEndSellActionPerformed

